# Você cria uma variável usando o operador de atribuição =. 
# Não é necessário declarar o tipo de dado antecipadamente, 
# pois o Python identifica isso automaticamente (tipagem dinâmica). 

nome = "Maria"      # String (texto)
idade = 25          # Integer (inteiro)
altura = 1.68       # Float (decimal)
e_estudante = True  # Boolean (verdadeiro/falso)

# Principais Tipos de Dados:

# Inteiros (int): Números inteiros, positivos ou negativos (ex: 10, -5).
# Decimais (float): Números com casas decimais (ex: 3.14, 2.0).
# Texto (str): Sequências de caracteres entre aspas simples ou duplas (ex: "Olá, Python!").
# Booleanos (bool): Valores lógicos, apenas True (Verdadeiro) ou False (Falso). 

# Regras de Nomeação:

# Para que seu código funcione e siga as boas práticas:
# Use snake_case: Nomes em letras minúsculas separados por underline (ex: meu_nome_completo).
# Início: Deve começar com uma letra ou underline (_). Nunca comece com números.
# Caracteres: Use apenas letras, números e underlines. Caracteres especiais e espaços não são permitidos.
# Palavras Reservadas: Não use nomes que o Python já utiliza para funções do sistema, como if, for, print ou while. 

# Dicas Úteis:

# Verificar o tipo: Use a função type() para saber qual o tipo de dado de uma variável.
# Exibir valor: Use a função print() para mostrar o conteúdo da variável no console.
# Alteração: Como o próprio nome diz, o valor de uma variável pode mudar ao longo da execução do script. 

