# Na divisão comum, quando dividimos 7 por 2, o resultado é 3,5. 
# No entanto, na divisão inteira, 7 dividido por 2 dá 3 e sobra 1.
# O operador % (módulo) ignora o quociente (o resultado 3) e nos 
# entrega apenas o resto (o 1).

# Para que serve na prática?
# Par ou Ímpar: Se numero % 2 for 0, é par. Se for 1, é ímpar.

# Múltiplos: Para saber se um número é divisível por outro 
# (ex: saber se um ano é bissexto dividindo por 4).

# Ciclos: Limitar um valor dentro de um intervalo 
# (ex: converter minutos totais em segundos de 0 a 59).